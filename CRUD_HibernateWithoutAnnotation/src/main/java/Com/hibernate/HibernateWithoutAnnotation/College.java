package Com.hibernate.HibernateWithoutAnnotation;

public class College {
	private int collegeRank;
	private String CollegeName;
	private String CollegeAddress;
	private Double CollegeFee;
	public int getCollegeRank() {
		return collegeRank;
	}
	public void setCollegeRank(int collegeRank) {
		this.collegeRank = collegeRank;
	}
	public String getCollegeName() {
		return CollegeName;
	}
	public void setCollegeName(String collegeName) {
		CollegeName = collegeName;
	}
	public String getCollegeAddress() {
		return CollegeAddress;
	}
	public void setCollegeAddress(String collegeAddress) {
		CollegeAddress = collegeAddress;
	}
	public Double getCollegeFee() {
		return CollegeFee;
	}
	public void setCollegeFee(Double collegeFee) {
		CollegeFee = collegeFee;
	}
	
	
	

}
